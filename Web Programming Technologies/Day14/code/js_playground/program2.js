// array destructuring

const array = [10, 20, 30]
console.log(`first = ${array[0]}, second = ${array[1]}, third = ${array[2]}`)

// here n1, n2 and n3 variables will be created with values from
// zeroth, first and second positions from array
const [n1, n2, n3] = array
console.log(`n1 = ${n1}, n2 = ${n2}, n3 = ${n3}`)
